/**
 * Pack utility functions.
 */

/**
 * Create an empty pack file (header + checksum only, 0 objects).
 */
export function createEmptyPack(): Uint8Array {
  return new Uint8Array([
    0x50,
    0x41,
    0x43,
    0x4b, // "PACK"
    0x00,
    0x00,
    0x00,
    0x02, // Version 2
    0x00,
    0x00,
    0x00,
    0x00, // 0 objects
    // SHA-1 checksum of the above header
    0x02,
    0x9d,
    0x08,
    0x82,
    0x3b,
    0xd8,
    0xa8,
    0xea,
    0xb5,
    0x10,
    0xad,
    0x6a,
    0xc7,
    0x5c,
    0x82,
    0x3c,
    0xfd,
    0x3e,
    0xd3,
    0x1e,
  ]);
}
